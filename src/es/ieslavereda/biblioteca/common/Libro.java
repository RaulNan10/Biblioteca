package es.ieslavereda.biblioteca.common;

import java.util.*;

public abstract class Libro {

	private String autor;
	private String titulo;
	private int isbn;

	
	public Libro(String autor, String titulo, int isbn) {
		super();
		this.autor = autor;
		this.titulo = titulo;
		this.isbn = isbn;
		
	}
	
	
	
	
	
}
